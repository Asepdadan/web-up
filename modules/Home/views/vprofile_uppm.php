                        <h3>UNIT PENELITIAN DAN PENGABDIAN KEPADA MASYARAKAT - POLBAN</h3>

                        <p style="text-align: justify">Penelitian saat ini memegang peranan yang tak dapat dinafikan selaku kegiatan pilar sebuah perguruan tinggi. Berlin University, yang diakui sebagai universitas riset pertama, berdiri pada tahun 1809. Dibandingkan universitas berbasis pengajaran yang telah ada sekurangnya sejak abad pertengahan, diawali University of Bologna di Italia, yang berdiri pada 1088, maka universitas riset boleh terbilang seumur jagung. Kendati demikian, sejak dekade-dekade awal kancahnya, universitas riset telah memberi kontribusi yang berarti bagi masyarakat di mana ia bernaung. Mengikuti Berlin University, universitas-universitas riset berkembang di Jerman, menghasilkan lulusan- lulusan yang di kemudian hari turut memajukan industri negerinya secara internasional, khususnya industri farmasinya. Di Amerika Serikat, universitas- universitas riset generasi awal menghasilkan pelbagai penelitian yang mengembangkan teknologi pertanian, kehutanan, serta pertambangan bagi negaranya.<br>
Menyadari pentingnya universitas diberdayakan bukan hanya sebagai sentra ajar melainkan juga sentra riset, yang penting peranannya untuk menggali pengetahuan pengetahuan strategis, berbagai pemegang kepentingan baik negara maupun swasta dalam perjalanannya aktif dalam mendorong pengembangan riset melalui institusi ini. AS sendiri, sebagai sebuah negara yang perekonomiannya bertumpu pada sektor pertanian, sejak 1862 menerapkan seperangkat perundang- undangan yang mendorong pendanaan penelitian universitas yang berpotensi mengembangkan produktivitas sektor ini. Pendanaan riset ini pun, seiring pertumbuhan perekonomian negaranya, dari waktu ke waktu terus meningkat; hingga secara khusus pada 1940 ke 1945 pendanaan pemerintah AS untuk perguruan tinggi meningkat lebih dari sepuluh kali lipat dari $39 juta menjadi $524 juta.<br>
Kini fokus kepada riset telah menjadi norma bagi universitas-universitas di berbagai penjuru dunia. Pengetahuan dan pengembangan yang dihasilkan oleh penelitian terbukti meningkatkan daya kompetisi pihak-pihak yang mungkin untuk memanfaatkannya. Setiap pekerjaan dalam university research park, atau science park, dari laporan penelitian Association of University Research Park, setara produktivitasnya dengan 2,57 pekerjaan di lapangan kerja rata-rata.
Research university pun telah menjadi sebuah predikat yang jamak disematkan kepada perguruan tinggi yang unggul di bidang aktivitas riset. Bagi perguruan tinggi bersangkutan, memperoleh predikat research university telah menjadi satu kehormatan tersendiri. Dengan demikian, boleh dikatakan ia telah memberi sumbangsih riil bagi pengembangan ranah keilmuan; ia telah berkontribusi bagi pemahaman umat manusia akan kehidupan yang penting halnya untuk kepentingan yang luas. Dicetuskan pertama kali dalam Carnegie Classification of Institutions of Higher Education pada 1994, beberapa kriteria dari research university antara lain:<BR>

1.	Menyediakan program-program sarjana secara lengkap.<br>
2.	Berkomitmen pada pendidikan pascasarjana lewat program doktoral.<br>
3.	Memberikan prioritas tinggi kepada penelitian.<br>
4.	Memberikan 50 atau lebih gelar doktor dalam setahun.<br>
5.	Memperoleh pendanaan pemerintahan sekurangnya $40 juta dalam setahun.<br>
Dalam perjalanannya, beberapa kriteria ini terbukti tidak baku. Jaringan perguruan tinggi lain, seperti AREARU (The Association of East Asian Research Universities),yang melibatkan Beijing University, Tokyo University, Seoul University, dan lain lain., maupun IARU (International Alliance of Research Universities) yang mencakup Australian National University, Oxford University, Cambridge University, dll. mencetuskan konsep universitas riset yang berbeda.
Carnegie sendiri mengubah kriterianya dalam menentukan universitas riset. Kriteria- kriteria Carnegie sejak 2005 antara lain; belanja penelitian dan pengembangan di bidang eksakta dan teknik, belanja penelitian dan pengembangan di bidang non-eksakta dan teknik, jumlah staf peneliti eksakta dan teknik, jumlah doktor di bidang humaniora, bidang ilmu sosial, bidang sains, teknologi, teknik, dan matematika serta bidang-bidang lainnya dan yang dihasilkan.

                
                  
